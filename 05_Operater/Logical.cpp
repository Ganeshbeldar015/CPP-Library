// There are three main logical operators in C++: 

// Logical AND (&&):
// This operator returns true only if both operands are true. 
// If either operand is false, the entire expression evaluates to false.
// Example: (x > 0 && x < 10) is true only if x is both greater than 0 AND less than 10. 

// Logical OR (||):
// This operator returns true if at least one of the operands is true. 
// It only returns false if both operands are false.
// Example: (age < 18 || isStudent) is true if the age is less than 18 OR if the person isStudent.

// Logical NOT (!):
// This is a unary operator that reverses the boolean value of its operand.
// If the operand is true, ! makes it false. If the operand is false, ! makes it true.
// Example: !isLoggedIn is true if the user is not logged in